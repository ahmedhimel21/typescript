{
  // basic data types

  // number
  let age: number = 22;
  // age = '23';
  age = 23;

  // string
  let firstName: string = "Himel";

  // boolean
  let isSingle: boolean = true;

  // undefined
  let x: undefined = undefined;

  // null
  let y: null = null;

  // non primitive data type

  // array of string
  let friends: string[] = ["x", "y"];

  // array of numbers
  let numbers: number[] = [1, 3, 5];

  // tuple
  let coordinates: [number, number] = [1, 5];

  let ageName : [number, string, boolean] = [23, 'Mr. X', true]
}
